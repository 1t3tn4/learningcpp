#include <iostream>

using namespace std;

int main() {

	int number1 = 10;
	int number2 = 4;

	cout << "Number1 + Number2: " << number1 + number2 << endl;
	cout << "Number1 - Number2: " << number1 - number2 << endl;
	cout << "Number1 * Number2: " << number1 * number2 << endl;
	cout << "Number1 / Number2: " << float(number1) / number2 << endl;

	return 0;
}
